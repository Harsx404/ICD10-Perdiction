ICD_CODE_TO_DESCRIPTION: dict[str, str] = {
    item["icd_code"]: item["icd_description"]
    for item in ICD_TERM_CATALOG
}
ICD_CODE_TO_LABEL: dict[str, str] = {
    item["icd_code"]: item["label"]
    for item in ICD_TERM_CATALOG
}

# Maps every alias (lowercase) → canonical label so free-form LLM text can be normalized.
ALIAS_TO_LABEL: dict[str, str] = {}
# Maps entity_type → alias (lowercase) → canonical label for type-restricted normalization.
ALIAS_TO_LABEL_BY_TYPE: dict[str, dict[str, str]] = {}
for _item in TERM_CATALOG:
    for _alias in _item.get("aliases", []):
        _lower_alias = _alias.lower()
        ALIAS_TO_LABEL[_lower_alias] = _item["label"]
        _etype = _item.get("entity_type", "")
        ALIAS_TO_LABEL_BY_TYPE.setdefault(_etype, {})[_lower_alias] = _item["label"]


def normalize_entity_label(text: str, restrict_types: tuple[str, ...] | None = None) -> str:
    """Return the canonical catalog label for *text*, or *text* unchanged if unknown.

    *restrict_types* limits alias matching to catalog entries of those entity types,
    preventing e.g. a severity alias ('uncontrolled') from shadowing a disease alias
    ('diabetes') when normalizing a disease list.
    """
    lower = text.lower().strip()

    if restrict_types is None:
        alias_map = ALIAS_TO_LABEL
    else:
        alias_map = {}
        for rtype in restrict_types:
            alias_map.update(ALIAS_TO_LABEL_BY_TYPE.get(rtype, {}))

    # Exact alias match first.
    if lower in alias_map:
        return alias_map[lower]

    # Longest-alias substring match to avoid short aliases (e.g. 'dm2') overshadowing
    # longer, more specific ones (e.g. 'type 2 diabetes').
    best_alias = ""
    best_label = ""
    for alias, label in alias_map.items():
        if alias in lower and len(alias) > len(best_alias):
            best_alias = alias
            best_label = label
    if best_label:
        return best_label

    return text

